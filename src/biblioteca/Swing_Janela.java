    private void btnBuscarPorIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPorIdActionPerformed
        
        UsuarioDAO dao = new UsuarioDAO();
        DefaultTableModel modelo = (DefaultTableModel) tabelaListar.getModel();
        
        try{
            modelo.setRowCount(0);
            Usuario usuarioId = dao.buscarUsuarioPorId(Integer.parseInt(txtId.getText().trim()));
            
            if(usuarioId != null){
                modelo.addRow(new Object[]{usuarioId.getId(), usuarioId.getNome(), usuarioId.getEmail(),
                usuarioId.getTelefone(), usuarioId.getTipo_usuario()});
                lblMensagem2.setText("Usuario encontrado!");
                new Timer (4000, e -> lblMensagem2.setText("")).start();
                
            }else{
                lblMensagem2.setText("Usuario nao encontrado !");
                new Timer (7000, e -> lblMensagem2.setText("")).start();
   
            }
        }catch(SQLException e){
            lblMensagem2.setText("Erro no banco"+ e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarPorIdActionPerformed

    private void btnDeletarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarUsuarioActionPerformed
         UsuarioDAO dao = new UsuarioDAO();
         
         try{
             Usuario usuario = dao.buscarUsuarioPorId(Integer.parseInt(txtId.getText().trim()));
             
             if(usuario != null){
                dao.deletarUsuario(Integer.parseInt(txtId.getText().trim()));
                lblMensagem2.setText("Usuario deletado com sucesso!");
                new Timer (4000, e -> lblMensagem2.setText("")).start();
             }else{
                lblMensagem2.setText("Usuario nao encontrado!");
                new Timer (7000, e -> lblMensagem2.setText("")).start(); 
             }
         }catch(SQLException e){
             lblMensagem2.setText("Erro no banco"+ e.getMessage());
         }
    }//GEN-LAST:event_btnDeletarUsuarioActionPerformed

