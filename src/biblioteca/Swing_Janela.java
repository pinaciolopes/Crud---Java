    private void Criar_UsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Criar_UsuarioActionPerformed
        UsuarioDAO dao = new UsuarioDAO();
        Usuario usuario = new Usuario(0,txtNome.getText().trim(), txtEmail.getText().trim(), txtTelefone.getText().trim(),
                txtTipo_Usuario.getText().trim());
        
        try{
            dao.criarUsuario(usuario);
            
            if (txtNome.getText().isEmpty()
                || txtEmail.getText().isEmpty()
                || txtTelefone.getText().isEmpty()
                || txtTipo_Usuario.getText().isEmpty()){
                
                lblMensagem.setText("Preencha os campos");
                
                txtNome.setText("");
                txtEmail.setText("");
                txtTelefone.setText("");
                txtTipo_Usuario.setText("");
                
            } else {
                lblMensagem.setText("Usuario Cadastrado");
                txtNome.setText("");
                txtEmail.setText("");
                txtTelefone.setText("");
                txtTipo_Usuario.setText("");
            }        
           
                    
        }catch(SQLException e){
            System.out.println("Deu errado " + e.getMessage());
            lblMensagem.setText("Erro"+ e.getMessage());
            txtNome.setText("");
            txtEmail.setText("");
            txtTelefone.setText("");
            txtTipo_Usuario.setText("");
        }
    
    }//GEN-LAST:event_Criar_UsuarioActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        UsuarioDAO dao = new UsuarioDAO();
        DefaultTableModel modelo = (DefaultTableModel) tabelaListar.getModel();
        
        try{
            List <Usuario> lista = dao.listarUsuarios();
            modelo.setRowCount(0);
            
            if(lista.isEmpty()){
                lblMensagem2.setText("Ninguem no banco");
                new Timer (7000, e -> lblMensagem2.setText("")).start();
            }else{
            for(Usuario u : lista){
                modelo.addRow(new Object[] {u.getId(), u.getNome(), u.getEmail(), u.getTelefone(), u.getTipo_usuario()});
                lblMensagem2.setText("Usuarios listados");
                new Timer (4000, e -> lblMensagem2.setText("")).start();
                
                }
            }
               
        }catch(SQLException e){
            lblMensagem2.setText("Erro no banco"+ e.getMessage());
        }
           
    }//GEN-LAST:event_jButton1ActionPerformed

